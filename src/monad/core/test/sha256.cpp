#include <gtest/gtest.h>
#include <monad/core/byte_string.hpp>
#include <monad/core/sha256.hpp>

#include <string>

using namespace monad;

using hash = monad::byte_string_fixed<32>;

TEST(SHA256, basic_string_test)
{
    monad::byte_string_fixed<3> message = {'l', 'o', 'l'};
    hash output;
    crypto::sha256(output.data(), message.data(), message.size());
    hash expected{0x07, 0x12, 0x3e, 0x1f, 0x48, 0x23, 0x56, 0xc4,
                  0x15, 0xf6, 0x84, 0x40, 0x7a, 0x3b, 0x87, 0x23,
                  0xe1, 0x0b, 0x2c, 0xbb, 0xc0, 0xb8, 0xfc, 0xd6,
                  0x28, 0x2c, 0x49, 0xd3, 0x7c, 0x9c, 0x1a, 0xbc};
    EXPECT_EQ(output, expected);
}

TEST(SHA256, empty_string_test)
{
    monad::byte_string_fixed<0> message = {};
    hash output;
    crypto::sha256(output.data(), message.data(), message.size());
    hash expected{0xe3, 0xb0, 0xc4, 0x42, 0x98, 0xfc, 0x1c, 0x14,
                  0x9a, 0xfb, 0xf4, 0xc8, 0x99, 0x6f, 0xb9, 0x24,
                  0x27, 0xae, 0x41, 0xe4, 0x64, 0x9b, 0x93, 0x4c,
                  0xa4, 0x95, 0x99, 0x1b, 0x78, 0x52, 0xb8, 0x55};
    EXPECT_EQ(output, expected);
}

TEST(SHA256, big_string_test)
{
    monad::byte_string_fixed<43> message = {
        'l', 'o', 'l', 'o', 'l', 'o', 'l', 'o', 'l', 'o', 'l',
        'o', 'l', 'o', 'l', 'o', 'l', 'o', 'l', 'o', 'l', 'o',
        'l', 'o', 'l', 'o', 'l', 'o', 'l', 'o', 'l', 'o', 'l',
        'o', 'l', 'o', 'l', 'o', 'l', 'o', 'l', 'o', 'l'};
    hash output;
    crypto::sha256(output.data(), message.data(), message.size());
    hash expected{0x96, 0xab, 0x3e, 0x08, 0x0e, 0x14, 0x6a, 0x66,
                  0xef, 0xc2, 0x73, 0x43, 0x51, 0xef, 0x41, 0x89,
                  0x22, 0x50, 0x0c, 0xfd, 0xcb, 0xba, 0xcc, 0xf5,
                  0x44, 0xf7, 0xe4, 0xe1, 0x36, 0x1a, 0x45, 0x71};
    EXPECT_EQ(output, expected);
}
